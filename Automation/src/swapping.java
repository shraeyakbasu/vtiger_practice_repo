
public class swapping {

	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		System.out.println("value of a is "+a);
		System.out.println("value of b is "+b);
		b=a+b;
		a=b-a;
		b=b-a;
		System.out.println("value of a is "+a);
		System.out.println("value of b is "+b);
		
		

	}

}
